async function fetchChapterData(chapter) {
  // Bible API alternative (gratuite, pas de cl√©)
  const url = `https://bible-api.com/john+${chapter}?translation=web`;
  
  return new Promise((resolve, reject) => {
    https.get(url, (res) => {
      let data = '';
      
      res.on('data', (chunk) => {
        data += chunk;
      });
      
      res.on('end', () => {
        try {
          const json = JSON.parse(data);
          resolve(json);
        } catch (e) {
          reject(e);
        }
      });
    }).on('error', reject);
  });
}

