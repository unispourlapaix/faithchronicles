# Fonction pour remplir un chapitre
def fill_chapter(chapter_num):
    if chapter_num not in hebrew_chapters:
        print(f"⚠️  Chapitre {chapter_num}: Données non disponibles")
        return False
    
    filename = f"john-{str(chapter_num).zfill(2)}-he.js"
    filepath = os.path.join(base_path, filename)
    
    if not os.path.exists(filepath):
        print(f"❌ Fichier {filename} non trouvé")
        return False
    
    # Lire le fichier
    with open(filepath, 'r', encoding='utf-8') as f:
        content = f.read()
    
    # Obtenir les versets
    verses = hebrew_chapters[chapter_num]
    
    # Remplacer les placeholders
    modified_content = content
    for i, verse_text in enumerate(verses, 1):
        placeholder = f'"text": "[Texte hébreu du verset {i}]"'
        replacement = f'"text": "{verse_text}"'
        modified_content = modified_content.replace(placeholder, replacement)
    
    # Supprimer le commentaire TEMPLATE
    modified_content = modified_content.replace("// ⚠️ TEMPLATE - Texte à compléter\n", "")
    
    # Écrire le fichier modifié
    with open(filepath, 'w', encoding='utf-8') as f:
        f.write(modified_content)
    
    print(f"✅ {filename} rempli ({len(verses)} versets)")
    return True

